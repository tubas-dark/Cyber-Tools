# Função 2: Leitor IP-Trace
def leitor_ip_trace():
    ip = input(colored("Digite o IP para localização: ", "yellow"))
    response = requests.get(f'https://ipinfo.io/{ip}/json')
    if response.status_code == 200:
        data = response.json()
        print(colored(f"Informações sobre o IP {ip}:", "yellow"))
        print(colored(f"Localização: {data['city']}, {data['region']}, {data['country']}", "yellow"))
        print(colored(f"Organização: {data['org']}", "yellow"))
    else:
        print(colored("Erro ao localizar o IP.", "red"))
    voltar_menu()

