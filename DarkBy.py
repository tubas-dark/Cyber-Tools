# Função 4: Velocidade de Rede (Simulador de ms)
def velocidade_rede():
    st = speedtest.Speedtest()
    st.get_best_server()
    download = st.download() / 1_000_000
    upload = st.upload() / 1_000_000
    ping_simulado = 100  # Simulação de ping fixo de 100ms
    print(colored(f"Velocidade de Download: {download:.2f} Mbps", "yellow"))
    print(colored(f"Velocidade de Upload: {upload:.2f} Mbps", "yellow"))
    print(colored(f"Simulação de Ping: {ping_simulado} ms", "yellow"))
    print(colored(f"Velocidade de Rede Simulada: {download:.2f} Mbps (Download), {upload:.2f} Mbps (Upload), {ping_simulado} ms (Ping)", "yellow"))
    voltar_menu()

# Função 5: Longitude/Latitude
def longitude_latitude():
    print(colored("1. Minha longitude/latitude", "yellow"))
    print(colored("2. Scan longitude/latitude", "yellow"))
    
    escolha = input(colored("Escolha uma opção: ", "yellow"))
    
    if escolha == "1":
        g = geocoder.ip('me')
        if g.latlng:
            print(colored(f"Latitude: {g.latlng[0]}", "yellow"))
            print(colored(f"Longitude: {g.latlng[1]}", "yellow"))
        else:
            print(colored("Não foi possível obter a sua localização.", "red"))
    elif escolha == "2":
        ip = input(colored("Digite o IP para localizar a longitude/latitude: ", "yellow"))
        g = geocoder.ip(ip)
        if g.latlng:
            print(colored(f"Latitude: {g.latlng[0]}", "yellow"))
            print(colored(f"Longitude: {g.latlng[1]}", "yellow"))
        else:
            print(colored("Erro ao localizar o IP ou a localização não foi encontrada.", "red"))
    else:
        print(colored("Opção inválida!", "red"))
    voltar_menu()

